package de.cosmocode.commons;

import java.util.concurrent.TimeUnit;

/**
 * A unit enum similiar to {@link TimeUnit} for
 * binary units used for storage sizes.
 *
 * @since 1.14
 * @author Willi Schoenborn
 */
enum BinaryUnit {

    KIBI,
    
    MIBI,
    
    GIBI,
    
    TEBI,
    
    PEBI,
    
    EXIBI,
    
    ZEBI,
    
    YOBI;
    
}
